export default function App() {
  const [started, setStarted] = useState(false);
  const [showFirstPoem, setShowFirstPoem] = useState(false);
  const [activeWord, setActiveWord] = useState(null);
  const [showLetter, setShowLetter] = useState(false);
  const [showRose, setShowRose] = useState(false);

  const music = useMemo(
    () =>
      new Howl({
        src: ['/echo-lubvi.mp3'],
        loop: true,
        volume: 0.5,
        html5: true,
      }),
    []
  );

  useEffect(() => {
    let timer;
    if (started) {
      timer = setTimeout(() => {
        setShowRose(true);
      }, 2600);
    } else {
      setShowRose(false);
    }
    return () => clearTimeout(timer);
  }, [started]);

  const handleStart = () => {
    if (!music.playing()) {
      music.play();
    }
    setShowFirstPoem(true);
  };

  const handleEnterScene = () => {
    setShowFirstPoem(false);
    setStarted(true);
  };

  const handleCloseAll = () => {
    if (activeWord) setActiveWord(null);
    if (showLetter) setShowLetter(false);
  };

  return (
    <div className="app-shell" onClick={handleCloseAll}>
      <div
        className="lake-bg"
        style={{
          backgroundImage: `linear-gradient(to bottom, rgba(4,8,18,0.12), rgba(4,8,18,0.48)), url(${lakeNightBg})`,
        }}
      />

      {started && (
        <>
          <div className="twinkle-layer" />

          <div className="hearts-layer">
            {heartPositions.map((item, index) => (
              <button
                key={index}
                className="heart-node"
                style={{
                  left: item.x,
                  top: item.y,
                  width: `${item.size}px`,
                  height: `${item.size}px`,
                  animationDelay: `${(index % 7) * 0.35}s`,
                }}
                onClick={(e) => {
                  e.stopPropagation();
                  setActiveWord(starWords[index % starWords.length]);
                }}
                aria-label={starWords[index % starWords.length]}
                title={starWords[index % starWords.length]}
              >
                <img src={heartNebula} alt="" />
                <span className="heart-glow" />
              </button>
            ))}
          </div>

          {showRose && (
            <button
              className="rose-bouquet-button"
              onClick={(e) => {
                e.stopPropagation();
                setShowLetter(true);
              }}
              aria-label="Открыть письмо"
              title="Открыть письмо"
            >
              <img src={roseBouquet} alt="Букет роз" />
              <span className="rose-hint">Коснись роз</span>
            </button>
          )}
        </>
      )}

      <Overlay
        started={started}
        showFirstPoem={showFirstPoem}
        onStart={handleStart}
        onEnterScene={handleEnterScene}
        firstPoem={firstPoem}
        activeWord={activeWord}
        onCloseWord={() => setActiveWord(null)}
        showLetter={showLetter}
        secondPoem={secondPoem}
        onCloseLetter={() => setShowLetter(false)}
      />
    </div>
  );
}